exports.nestedReviews = (req, res, next) => {
  if (req.params.tourId) req.query.tour = req.params.tourId;
  if (req.params.userId) req.query.user = req.params.userId;

  next();
};

exports.getCheckoutSession = catchAsync(async (req, res, next) => {
  // 1. Get the currently booked tour
  const tour = await Tour.findById(req.params.tourId);

  // 2. Check if the tour has been booked before
  const bookedTour = await Booking.findOne({
    user: req.user.id,
    tour: req.params.tourId,
  });

  if (bookedTour) {
    return next(new AppError(`This tour has already been booked before!`, 401));
  }

  // 3. Create checkout session
  const session = await stripe.checkout.sessions.create({
    // success_url: `${req.protocol}://${req.get('host')}/my-tours/?tour=${
    //   req.params.tourId
    // }&user=${req.user.id}&price=${tour.price}`,
    success_url: `${req.protocol}://${req.get('host')}/my-tours?alert=booking`,
    cancel_url: `${req.protocol}://${req.get('host')}/tour/${tour.slug}`,
    customer_email: req.user.email,
    client_reference_id: req.params.tourId,
    line_items: [
      {
        price_data: {
          product_data: {
            name: `${tour.name} Tour`,
            description: tour.summary,
            images: [`https://www.natours.dev/img/tours/${tour.imageCover}`],
          },
          unit_amount: tour.price * 100,
          currency: 'usd',
        },
        quantity: 1,
      },
    ],
    mode: 'payment',
  });

  // 4. Create session as response
  res.status(200).json({
    status: 'success',
    session,
  });
});

